public class GalToLit {
    //This program converts gallons to litre.
    public static void main(String[] args) {
        double gallons; // call variables
        double litres;
        gallons = 10; // declare value of variables
        litres = gallons * 3.7854;// conversion of gallons to litres
        System.out.println(gallons + " gallons is " + litres + " litres. ");



    }
}
